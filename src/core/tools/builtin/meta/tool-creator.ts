/**
 * meta.tool-creator — Template-based tool creator for SUDO-AI.
 *
 * Unlike meta.skill-creator (which uses LLM to generate code), this tool
 * creates tools from pre-built TypeScript templates with specific parameters.
 * Faster, more reliable, zero token cost.
 *
 * Actions:
 *   create-file-tool      — Tool that reads/writes/transforms a specific file type
 *   create-api-tool       — Tool that calls an external HTTP API
 *   create-shell-tool     — Tool that runs a specific shell command safely
 *   create-db-tool        — Tool that queries/updates a SQLite database
 *   create-composite-tool — Tool that chains multiple existing tools
 *   list-custom           — List all custom-created tools
 *   test-tool             — Dry-run a custom tool with test params
 */

import type { ToolDefinition, ToolContext, ToolResult } from '../../types.js';
import { createLogger } from '../../../shared/logger.js';
import { existsSync, readdirSync, readFileSync } from 'node:fs';
import { writeFile, mkdir } from 'node:fs/promises';
import path from 'node:path';

const logger = createLogger('meta.tool-creator');

const CUSTOM_DIR = path.resolve('src/core/tools/builtin/custom');

// ---------------------------------------------------------------------------
// Name helpers
// ---------------------------------------------------------------------------

const NAME_PATTERN = /^[a-z][a-z0-9-]*\.[a-z][a-z0-9-]*$/;

/** Convert dot-namespaced name to a valid JS variable: "custom.my-tool" → "custom_my_tool" */
function toVarName(name: string): string {
  return name.replace(/[.-]/g, '_');
}

/** Convert dot-namespaced name to a filename: "custom.my-tool" → "custom-my-tool.ts" */
function toFileName(name: string): string {
  return `${name.replace(/\./g, '-')}.ts`;
}

// ---------------------------------------------------------------------------
// Validation
// ---------------------------------------------------------------------------

function validateToolName(toolName: string | undefined): ToolResult | null {
  if (!toolName?.trim()) {
    return { success: false, output: 'toolName is required.' };
  }
  if (!NAME_PATTERN.test(toolName)) {
    return {
      success: false,
      output: `toolName must match pattern <category>.<action> using lowercase letters, numbers, and hyphens. Got: "${toolName}"`,
    };
  }
  return null;
}

