function generateFileToolCode(
  toolName: string,
  description: string,
  targetPath: string,
  operations: string[],
): string {
  const varName = toVarName(toolName);
  const ops = JSON.stringify(operations);

  return `// Auto-generated by meta.tool-creator
import type { ToolDefinition, ToolContext, ToolResult } from '../../types.js';
import { readFileSync, writeFileSync, appendFileSync, unlinkSync, existsSync } from 'node:fs';
import { createLogger } from '../../../shared/logger.js';
import path from 'node:path';

const logger = createLogger('${toolName}');

export const ${varName}Tool: ToolDefinition = {
  name: '${toolName}',
  description: ${JSON.stringify(description)},
  category: 'meta' as const,
  timeout: 15_000,
  requiresConfirmation: true,
  parameters: {
    action: { type: 'string', required: true, description: 'Operation to perform.', enum: ${ops} },
    content: { type: 'string', description: 'Content for write/append operations.' },
  },
  async execute(params: Record<string, unknown>, ctx: ToolContext): Promise<ToolResult> {
    const action = params['action'] as string;
    const content = params['content'] as string | undefined;
    const filePath = path.resolve(${JSON.stringify(targetPath)});
    logger.info({ session: ctx.sessionId, action, filePath }, '${toolName} invoked');

    try {
      switch (action) {
        case 'read': {
          if (!existsSync(filePath)) return { success: false, output: \`File not found: \${filePath}\` };
          const data = readFileSync(filePath, 'utf8');
          return { success: true, output: data, artifacts: [{ path: filePath, action: 'read' }] };
        }
        case 'write': {
          if (!content) return { success: false, output: 'content is required for write.' };
          writeFileSync(filePath, content, 'utf8');
          return { success: true, output: \`Written \${content.length} chars to \${filePath}\`, artifacts: [{ path: filePath, action: 'created' }] };
        }
        case 'append': {
          if (!content) return { success: false, output: 'content is required for append.' };
          appendFileSync(filePath, content, 'utf8');
          return { success: true, output: \`Appended \${content.length} chars to \${filePath}\`, artifacts: [{ path: filePath, action: 'modified' }] };
        }
        case 'delete': {
          if (!existsSync(filePath)) return { success: false, output: \`File not found: \${filePath}\` };
          unlinkSync(filePath);
          return { success: true, output: \`Deleted \${filePath}\`, artifacts: [{ path: filePath, action: 'deleted' }] };
        }
        default:
          return { success: false, output: \`Unknown action: \${action}\` };
      }
    } catch (err) {
      const msg = err instanceof Error ? err.message : String(err);
      logger.error({ err: msg }, '${toolName} error');
      return { success: false, output: \`Error: \${msg}\` };
    }
  },
};
`;
}

function generateApiToolCode(
  toolName: string,
  description: string,
  baseUrl: string,
  method: string,
  headers: Record<string, string>,
  bodyTemplate: string,
): string {
  const varName = toVarName(toolName);

  // Transform headers so values referencing env vars use process.env at runtime
  const headerEntries = Object.entries(headers).map(([k, v]) => {
    // If the value looks like an env var reference (e.g. "$MY_API_KEY"), resolve at runtime
    if (v.startsWith('$')) {
      return `    '${k}': process.env['${v.slice(1)}'] ?? ''`;
    }
    return `    '${k}': ${JSON.stringify(v)}`;
  });
  const headersCode = headerEntries.length > 0
    ? `{\n${headerEntries.join(',\n')}\n  }`
    : '{}';

  return `// Auto-generated by meta.tool-creator
import type { ToolDefinition, ToolContext, ToolResult } from '../../types.js';
import { createLogger } from '../../../shared/logger.js';

const logger = createLogger('${toolName}');

export const ${varName}Tool: ToolDefinition = {
  name: '${toolName}',
  description: ${JSON.stringify(description)},
  category: 'meta' as const,
  timeout: 30_000,
  requiresConfirmation: true,
  parameters: {
    body: { type: 'object', description: 'Request body (JSON).', properties: {} },
    pathSuffix: { type: 'string', description: 'Optional path appended to the base URL.' },
    queryParams: { type: 'object', description: 'URL query parameters.', properties: {} },
  },
  async execute(params: Record<string, unknown>, ctx: ToolContext): Promise<ToolResult> {
    const body = params['body'] as Record<string, unknown> | undefined;
    const pathSuffix = (params['pathSuffix'] as string | undefined) ?? '';
    const queryParams = params['queryParams'] as Record<string, string> | undefined;
    logger.info({ session: ctx.sessionId }, '${toolName} invoked');

    try {
      let url = ${JSON.stringify(baseUrl)} + pathSuffix;
      if (queryParams && Object.keys(queryParams).length > 0) {
        const qs = new URLSearchParams(queryParams).toString();
        url += (url.includes('?') ? '&' : '?') + qs;
      }

      const headers: Record<string, string> = ${headersCode};

      const method = ${JSON.stringify(method.toUpperCase())};
      const hasBody = method !== 'GET' && method !== 'HEAD' && body;

      const response = await fetch(url, {
        method,
        headers: {
          'Content-Type': 'application/json',
          ...headers,
        },
        ...(hasBody ? { body: JSON.stringify(body) } : {}),
        signal: ctx.signal,
      });

      const text = await response.text();
      let data: unknown;
      try { data = JSON.parse(text); } catch { data = text; }

      if (!response.ok) {
        return { success: false, output: \`HTTP \${response.status}: \${text.slice(0, 500)}\`, data };
      }
      return { success: true, output: typeof data === 'string' ? data.slice(0, 2000) : JSON.stringify(data, null, 2).slice(0, 2000), data };
    } catch (err) {
      const msg = err instanceof Error ? err.message : String(err);
      logger.error({ err: msg }, '${toolName} error');
      return { success: false, output: \`Error: \${msg}\` };
    }
  },
};
`;
}

function generateShellToolCode(
  toolName: string,
  description: string,
  command: string,
  allowedArgs: string[],
  timeout: number,
): string {
  const varName = toVarName(toolName);

  return `// Auto-generated by meta.tool-creator
import type { ToolDefinition, ToolContext, ToolResult } from '../../types.js';
import { createLogger } from '../../../shared/logger.js';
import { execFile } from 'node:child_process';
import { promisify } from 'node:util';

const execFileAsync = promisify(execFile);
const logger = createLogger('${toolName}');

const ALLOWED_ARGS: string[] = ${JSON.stringify(allowedArgs)};
const TIMEOUT_MS = ${timeout};

export const ${varName}Tool: ToolDefinition = {
  name: '${toolName}',
  description: ${JSON.stringify(description)},
  category: 'meta' as const,
  timeout: ${timeout + 5000},
  requiresConfirmation: true,
  parameters: {
    args: { type: 'array', description: 'Arguments to pass to the command. Must be from the allowed list.', items: { type: 'string', description: 'Argument value.' } },
  },
  async execute(params: Record<string, unknown>, ctx: ToolContext): Promise<ToolResult> {
    const args = (params['args'] as string[] | undefined) ?? [];
    logger.info({ session: ctx.sessionId, args }, '${toolName} invoked');

    try {
      // Validate args against allowlist
      if (ALLOWED_ARGS.length > 0) {
        for (const arg of args) {
          if (!ALLOWED_ARGS.includes(arg)) {
            return { success: false, output: \`Argument not allowed: "\${arg}". Allowed: \${ALLOWED_ARGS.join(', ')}\` };
          }
        }
      }

      const { stdout, stderr } = await execFileAsync(${JSON.stringify(command)}, args, {
        timeout: TIMEOUT_MS,
        cwd: ctx.workingDir,
        env: process.env,
      });

      const output = (stdout + (stderr ? \`\\nSTDERR: \${stderr}\` : '')).trim();
      return { success: true, output: output.slice(0, 5000) };
    } catch (err) {
      const msg = err instanceof Error ? err.message : String(err);
      logger.error({ err: msg }, '${toolName} error');
      return { success: false, output: \`Error: \${msg}\` };
    }
  },
};
`;
}

function generateDbToolCode(
  toolName: string,
  description: string,
  dbPath: string,
  queries: Array<{ name: string; sql: string }>,
): string {
  const varName = toVarName(toolName);
  const queryEnum = queries.map(q => q.name);
  const queryMap = queries.reduce<Record<string, string>>((acc, q) => {
    acc[q.name] = q.sql;
    return acc;
  }, {});

  return `// Auto-generated by meta.tool-creator
import type { ToolDefinition, ToolContext, ToolResult } from '../../types.js';
import { createLogger } from '../../../shared/logger.js';
import Database from 'better-sqlite3';
import path from 'node:path';

const logger = createLogger('${toolName}');

const DB_PATH = path.resolve(${JSON.stringify(dbPath)});
const QUERIES: Record<string, string> = ${JSON.stringify(queryMap, null, 2)};

export const ${varName}Tool: ToolDefinition = {
  name: '${toolName}',
  description: ${JSON.stringify(description)},
  category: 'meta' as const,
  timeout: 15_000,
  requiresConfirmation: true,
  parameters: {
    query: { type: 'string', required: true, description: 'Named query to execute.', enum: ${JSON.stringify(queryEnum)} },
    params: { type: 'object', description: 'Named parameters for the SQL query (e.g. { ":name": "value" }).', properties: {} },
  },
  async execute(params: Record<string, unknown>, ctx: ToolContext): Promise<ToolResult> {
    const queryName = params['query'] as string;
    const queryParams = (params['params'] as Record<string, unknown> | undefined) ?? {};
    logger.info({ session: ctx.sessionId, query: queryName }, '${toolName} invoked');

    try {
      const sql = QUERIES[queryName];
      if (!sql) return { success: false, output: \`Unknown query: \${queryName}. Available: \${Object.keys(QUERIES).join(', ')}\` };

      const db = new Database(DB_PATH, { readonly: sql.trim().toUpperCase().startsWith('SELECT') });
      try {
        const isSelect = sql.trim().toUpperCase().startsWith('SELECT');
        if (isSelect) {
          const rows = db.prepare(sql).all(queryParams);
          return { success: true, output: JSON.stringify(rows, null, 2).slice(0, 5000), data: rows };
        } else {
          const result = db.prepare(sql).run(queryParams);
          return { success: true, output: \`Rows affected: \${result.changes}\`, data: { changes: result.changes, lastInsertRowid: result.lastInsertRowid } };
        }
      } finally {
        db.close();
      }
    } catch (err) {
      const msg = err instanceof Error ? err.message : String(err);
      logger.error({ err: msg }, '${toolName} error');
      return { success: false, output: \`Error: \${msg}\` };
    }
  },
};
`;
}

function generateCompositeToolCode(
  toolName: string,
  description: string,
  steps: Array<{ tool: string; params: Record<string, unknown> }>,
): string {
  const varName = toVarName(toolName);

  return `// Auto-generated by meta.tool-creator
import type { ToolDefinition, ToolContext, ToolResult } from '../../types.js';
import { createLogger } from '../../../shared/logger.js';

const logger = createLogger('${toolName}');

/**
 * Composite tool pipeline — chains multiple tool calls sequentially.
 * The execution plan is returned for the agent loop to run.
 */
const STEPS: Array<{ tool: string; params: Record<string, unknown> }> = ${JSON.stringify(steps, null, 2)};

export const ${varName}Tool: ToolDefinition = {
  name: '${toolName}',
  description: ${JSON.stringify(description)},
  category: 'meta' as const,
  timeout: 60_000,
  requiresConfirmation: true,
  parameters: {
    overrides: { type: 'object', description: 'Per-step param overrides keyed by 0-based index (e.g. { "0": { "extra": "val" } }).', properties: {} },
    dryRun: { type: 'boolean', description: 'If true, return the execution plan without running.', default: false },
  },
  async execute(params: Record<string, unknown>, ctx: ToolContext): Promise<ToolResult> {
    const overrides = (params['overrides'] as Record<string, Record<string, unknown>> | undefined) ?? {};
    const dryRun = (params['dryRun'] as boolean | undefined) ?? false;
    logger.info({ session: ctx.sessionId, steps: STEPS.length, dryRun }, '${toolName} invoked');

    const plan = STEPS.map((step, i) => ({
      step: i + 1,
      tool: step.tool,
      params: { ...step.params, ...(overrides[String(i)] ?? {}) },
    }));

    const planText = plan.map(s => \`Step \${s.step}: \${s.tool}(\${JSON.stringify(s.params)})\`).join('\\n');

    if (dryRun) {
      return { success: true, output: \`Composite tool plan (dry-run):\\n\${planText}\`, data: { plan } };
    }

    return {
      success: true,
      output: \`Composite tool execution plan (\${plan.length} steps):\\n\${planText}\\n\\nAgent: execute each step in order.\`,
      data: { plan },
    };
  },
};
`;
}

