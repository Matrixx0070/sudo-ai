async function ensureCustomDir(): Promise<void> {
  if (!existsSync(CUSTOM_DIR)) {
    await mkdir(CUSTOM_DIR, { recursive: true });
  }
}

async function createFileTool(
  toolName: string,
  description: string,
  templateParams: Record<string, unknown>,
): Promise<ToolResult> {
  const targetPath = templateParams['targetPath'] as string | undefined;
  const operations = (templateParams['operations'] as string[] | undefined) ?? ['read', 'write', 'append', 'delete'];

  if (!targetPath?.trim()) {
    return { success: false, output: 'templateParams.targetPath is required for create-file-tool.' };
  }

  const validOps = ['read', 'write', 'append', 'delete'];
  for (const op of operations) {
    if (!validOps.includes(op)) {
      return { success: false, output: `Invalid operation: "${op}". Valid: ${validOps.join(', ')}` };
    }
  }

  await ensureCustomDir();
  const code = generateFileToolCode(toolName, description, targetPath, operations);
  const filePath = path.join(CUSTOM_DIR, toFileName(toolName));
  await writeFile(filePath, code, 'utf8');

  return {
    success: true,
    output: `File tool "${toolName}" created at ${filePath}\nOperations: ${operations.join(', ')}\nTarget: ${targetPath}`,
    data: { toolName, filePath, operations, targetPath },
    artifacts: [{ path: filePath, action: 'created' }],
  };
}

async function createApiTool(
  toolName: string,
  description: string,
  templateParams: Record<string, unknown>,
): Promise<ToolResult> {
  const baseUrl = templateParams['baseUrl'] as string | undefined;
  const method = (templateParams['method'] as string | undefined) ?? 'GET';
  const headers = (templateParams['headers'] as Record<string, string> | undefined) ?? {};
  const bodyTemplate = (templateParams['bodyTemplate'] as string | undefined) ?? '';

  if (!baseUrl?.trim()) {
    return { success: false, output: 'templateParams.baseUrl is required for create-api-tool.' };
  }

  // Safety: warn if headers contain literal secrets
  for (const [key, val] of Object.entries(headers)) {
    if (!val.startsWith('$') && /^(sk-|Bearer |token |key )/i.test(val)) {
      return {
        success: false,
        output: `Header "${key}" appears to contain a literal secret. Use an env var reference like "$MY_API_KEY" instead.`,
      };
    }
  }

  await ensureCustomDir();
  const code = generateApiToolCode(toolName, description, baseUrl, method, headers, bodyTemplate);
  const filePath = path.join(CUSTOM_DIR, toFileName(toolName));
  await writeFile(filePath, code, 'utf8');

  return {
    success: true,
    output: `API tool "${toolName}" created at ${filePath}\nEndpoint: ${method.toUpperCase()} ${baseUrl}\nHeaders pull from env vars at runtime.`,
    data: { toolName, filePath, baseUrl, method },
    artifacts: [{ path: filePath, action: 'created' }],
  };
}

async function createShellTool(
  toolName: string,
  description: string,
  templateParams: Record<string, unknown>,
): Promise<ToolResult> {
  const command = templateParams['command'] as string | undefined;
  const allowedArgs = (templateParams['allowedArgs'] as string[] | undefined) ?? [];
  const timeout = (templateParams['timeout'] as number | undefined) ?? 15_000;

  if (!command?.trim()) {
    return { success: false, output: 'templateParams.command is required for create-shell-tool.' };
  }

  // Safety: block dangerous base commands
  const dangerous = ['rm', 'dd', 'mkfs', 'format', 'shutdown', 'reboot', 'kill', 'killall'];
  const baseCmd = path.basename(command).toLowerCase();
  if (dangerous.includes(baseCmd)) {
    return { success: false, output: `Command "${command}" is blocked for safety. Dangerous base commands are not allowed.` };
  }

  await ensureCustomDir();
  const code = generateShellToolCode(toolName, description, command, allowedArgs, timeout);
  const filePath = path.join(CUSTOM_DIR, toFileName(toolName));
  await writeFile(filePath, code, 'utf8');

  return {
    success: true,
    output: `Shell tool "${toolName}" created at ${filePath}\nCommand: ${command}\nAllowed args: ${allowedArgs.length > 0 ? allowedArgs.join(', ') : '(any)'}`,
    data: { toolName, filePath, command, allowedArgs },
    artifacts: [{ path: filePath, action: 'created' }],
  };
}

async function createDbTool(
  toolName: string,
  description: string,
  templateParams: Record<string, unknown>,
): Promise<ToolResult> {
  const dbPath = templateParams['dbPath'] as string | undefined;
  const rawQueries = templateParams['queries'] as Array<{ name: string; sql: string }> | undefined;

  if (!dbPath?.trim()) {
    return { success: false, output: 'templateParams.dbPath is required for create-db-tool.' };
  }
  if (!rawQueries || rawQueries.length === 0) {
    return { success: false, output: 'templateParams.queries is required (array of { name, sql }).' };
  }

  for (const q of rawQueries) {
    if (!q.name || !q.sql) {
      return { success: false, output: 'Each query must have a "name" and "sql" field.' };
    }
  }

  await ensureCustomDir();
  const code = generateDbToolCode(toolName, description, dbPath, rawQueries);
  const filePath = path.join(CUSTOM_DIR, toFileName(toolName));
  await writeFile(filePath, code, 'utf8');

  return {
    success: true,
    output: `DB tool "${toolName}" created at ${filePath}\nDatabase: ${dbPath}\nQueries: ${rawQueries.map(q => q.name).join(', ')}`,
    data: { toolName, filePath, dbPath, queries: rawQueries.map(q => q.name) },
    artifacts: [{ path: filePath, action: 'created' }],
  };
}

async function createCompositeTool(
  toolName: string,
  description: string,
  templateParams: Record<string, unknown>,
): Promise<ToolResult> {
  const steps = templateParams['steps'] as Array<{ tool: string; params: Record<string, unknown> }> | undefined;

  if (!steps || steps.length === 0) {
    return { success: false, output: 'templateParams.steps is required (array of { tool, params }).' };
  }

  for (const step of steps) {
    if (!step.tool) {
      return { success: false, output: 'Each step must have a "tool" field.' };
    }
  }

  await ensureCustomDir();
  const code = generateCompositeToolCode(toolName, description, steps);
  const filePath = path.join(CUSTOM_DIR, toFileName(toolName));
  await writeFile(filePath, code, 'utf8');

  return {
    success: true,
    output: `Composite tool "${toolName}" created at ${filePath}\nSteps: ${steps.map(s => s.tool).join(' → ')}`,
    data: { toolName, filePath, steps: steps.map(s => s.tool) },
    artifacts: [{ path: filePath, action: 'created' }],
  };
}

function listCustomTools(): ToolResult {
  if (!existsSync(CUSTOM_DIR)) {
    return { success: true, output: 'No custom tools directory yet. Create a tool first.', data: [] };
  }

  const files = readdirSync(CUSTOM_DIR).filter(f => f.endsWith('.ts'));
  if (files.length === 0) {
    return { success: true, output: 'No custom tools found.', data: [] };
  }

  const tools = files.map(f => {
    const filePath = path.join(CUSTOM_DIR, f);
    const content = readFileSync(filePath, 'utf8');
    const nameMatch = content.match(/name:\s*'([^']+)'/);
    const descMatch = content.match(/description:\s*(?:'([^']*)'|"([^"]*)")/);
    return {
      file: f,
      name: nameMatch?.[1] ?? f.replace('.ts', ''),
      description: (descMatch?.[1] ?? descMatch?.[2] ?? 'No description').slice(0, 100),
    };
  });

  const lines = tools.map(t => `  ${t.name} — ${t.description} [${t.file}]`);
  return {
    success: true,
    output: `${tools.length} custom tool(s):\n${lines.join('\n')}`,
    data: tools,
  };
}

function testTool(toolName: string | undefined, testParams: Record<string, unknown> | undefined): ToolResult {
  if (!toolName?.trim()) {
    return { success: false, output: 'toolName is required for test-tool.' };
  }

  const fileName = toFileName(toolName);
  const filePath = path.join(CUSTOM_DIR, fileName);

  if (!existsSync(filePath)) {
    return { success: false, output: `Custom tool file not found: ${filePath}` };
  }

  const content = readFileSync(filePath, 'utf8');

  // Extract metadata from the generated code
  const nameMatch = content.match(/name:\s*'([^']+)'/);
  const descMatch = content.match(/description:\s*(?:'([^']*)'|"([^"]*)")/);
  const paramsMatch = content.match(/parameters:\s*\{([\s\S]*?)\n\s*\},/);
  const hasConfirm = content.includes('requiresConfirmation: true');

  const report = [
    `Tool: ${nameMatch?.[1] ?? 'unknown'}`,
    `Description: ${(descMatch?.[1] ?? descMatch?.[2] ?? 'unknown').slice(0, 200)}`,
    `Requires confirmation: ${hasConfirm ? 'yes' : 'no'}`,
    `File: ${filePath}`,
    `Size: ${content.length} chars`,
    '',
    'Parameter schema (extracted):',
    paramsMatch ? paramsMatch[0].slice(0, 500) : '(could not extract)',
    '',
    `Test params provided: ${testParams ? JSON.stringify(testParams) : 'none'}`,
    '',
    'Syntax check: ' + (content.includes('export const') && content.includes('ToolDefinition') ? 'PASS (exports ToolDefinition)' : 'WARN (may not export correctly)'),
    'Import check: ' + (content.includes("from '../../types.js'") ? 'PASS' : 'WARN (missing types import)'),
    'Error handling: ' + (content.includes('catch (err)') ? 'PASS' : 'WARN (no error handling)'),
  ];

  return {
    success: true,
    output: `Dry-run report for "${toolName}":\n${report.join('\n')}`,
    data: {
      toolName,
      filePath,
      fileSize: content.length,
      syntaxOk: content.includes('export const') && content.includes('ToolDefinition'),
      hasErrorHandling: content.includes('catch (err)'),
      hasConfirmation: hasConfirm,
    },
  };
}

